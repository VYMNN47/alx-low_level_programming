#include "3-calc.h"

/**
 * op_add - adds 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: the difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}
